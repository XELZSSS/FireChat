const CHAT_INPUT_EMOJI_SET = new Set(
  CHAT_INPUT_EMOJI_GROUPS.flatMap((group) => (group.key === 'recent' ? [] : [...group.emojis]))
);

export const isAvailableChatInputEmoji = (emoji: string): boolean => {
  return CHAT_INPUT_EMOJI_SET.has(emoji);
};

export const resolveVisibleChatInputEmojiGroups = (
  recentEmojis: readonly string[]
): readonly ChatInputEmojiGroup[] => {
  return recentEmojis.length > 0
    ? [
        { ...CHAT_INPUT_EMOJI_GROUPS[0], emojis: [...recentEmojis] },
        ...CHAT_INPUT_EMOJI_GROUPS.slice(1),
      ]
    : CHAT_INPUT_EMOJI_GROUPS.slice(1);
};
